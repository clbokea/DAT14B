public class WhileLoop 
{
   public static void main(String[] args) 
   {     
   
       // while loop
      int i = 0;
      while(i <= 10)
      {
         System.out.println(i);
         i++;
      }
       
       // for loop
      for(int j = 0; j<=10;j++)
      {
         System.out.println(j);
      
      }
   
   }
}
