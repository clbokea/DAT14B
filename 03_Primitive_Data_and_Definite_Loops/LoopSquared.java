public class LoopSquared
{
   public static void main(String[] args)
   {
      System.out.println("1. Lav et for loop der printer dette: ");
      System.out.println(1 + "^2 = " + (1*1));
      System.out.println(2 + "^2 = " + (2*2));
      System.out.println(3 + "^2 = " + (3*3));
      System.out.println(4 + "^2 = " + (4*4));
      System.out.println(5 + "^2 = " + (5*5));
      System.out.println("osv. til 10");
      System.out.println();
      System.out.println();
      System.out.println("2. Lav et for loop der printer dette: ");
      System.out.println(10 + "^3 = " + (1*1));
      System.out.println(9 + "^3 = " + (2*2));
      System.out.println(8 + "^3 = " + (3*3));
      System.out.println(7 + "^3 = " + (4*4));
      System.out.println(5 + "^2 = " + (5*5));
      System.out.println("osv til 0");      
   }
}

      