public class If
{
   public static void main(String[] args)
   { 
      int number = 2;
         
      // udfoert 0 eller mange gange     
      if(number < 0)
      {
         System.out.println("Smaler than 0");
      }
      if(number > 0)
      {
         System.out.println("larger than 0");
      }
      if(number > 10)
      {
         System.out.println("Lager than 10");
      }
   }
}